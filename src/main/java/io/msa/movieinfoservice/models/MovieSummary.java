package io.msa.movieinfoservice.models;

public class MovieSummary {

    private String id;
    private String title;
    private String overview;

    public MovieSummary() {}

    public String getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getOverview() {
        return overview;
    }

}
